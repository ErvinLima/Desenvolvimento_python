# Colchetes [] indicam uma lista, seus elementos sao separados por virgula.

bicycles = ['trek', 'cannodale', 'redline', 'specialized']
print(bicycles)

# Listas são ordenaadas, portanto você consegue acessar qualquer elemento informando a posição que ele se encontra. 
# OBS: os elementos nunca começam na posição 1, sempre no 0.

print(bicycles[2])
# Aqui solicitei o item 2 da lista e ele retorna apenas o elemento presente naquela posicao 