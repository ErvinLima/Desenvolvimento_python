motocycles = ['honda', 'yamaha', 'suzuki']
print(motocycles)

# O metodo POP() remove o ultimo item de uma lista, mas ele me permite que eu trabalhe com esse item depois de remove-lo
# O termo pop deriva de pensar que a lista fosse uma pilha de itens e remover um item do topo da pilha, entao o topo da pilha corresponde ao final dela

popped_motocycle = motocycles.pop()
print(motocycles)
print(popped_motocycle)